
import Java


@_silgen_name("Android_init")
public func Android_init() -> Void {
  registerJavaClass(Manifest.self, fqn: "android/Manifest")    
  registerJavaClass(Manifestpermission.self, fqn: "android/Manifest$permission")    
  registerJavaClass(Manifestpermission_group.self, fqn: "android/Manifest$permission_group")    
}

